public class SwitchCase {
    public void WorkingwithSwitch(char it){
        switch (it){
            case 'a':
                System.out.println("A is Called");
                break;
            case 'b':
                System.out.println("B is Called");
                break;
            case 'c':
                System.out.println("C is called");
                break;
            default:
                System.out.println("Invalid Input");

        }
    }
}

